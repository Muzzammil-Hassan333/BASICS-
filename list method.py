numbers=[1,2,3,4,55,4,3,21,3]
print(numbers)
numbers.append(20)##append method is used to append a number at last index.
print(numbers)
numbers.insert(2,45)##insert method is used to insert a number at any index  of list.
print(numbers)
numbers.remove(55)##remove method is used to remove a number at any index location.
print(numbers)
print(numbers.index(4))##index method is used to find index location of any number.
print(numbers)
numbers.clear()##clear method is used to clear full numbers in list.
print(numbers)

